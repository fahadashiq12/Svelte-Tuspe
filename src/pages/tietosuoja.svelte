<h2>4. Rekisterin pitämisen peruste ja käyttötarkoitus</h2>
<p>Asiakkuuteen tai sen aloittamiseen vaaditut tiedot, joita käytämme asiakkuuden hoitamiseen ja yrityskohtaiseen markkinointiin.</p>
<p>Tiedot, joita henkilöstä tallennamme ovat lainmukaisia, kohtuullisia ja läpinäkyvää käsittelyn osalta. Henkilö voi milloin tahansa pyytää tietonsa saataville. Tallennamme vain tarpeellisen tiedon ja pyrimme pitämään tietomme täsmällisinä.</p>
